'''
知识点1 ：元组复制列表的正确方式
'''
a = [1,2,3]
b = (4,5,6)
b = a                   # b是a的引用,修改a也会修改b的值
a[0] = 0
print(b)                # [0, 2, 3]
b = a[:]                # b正确复制a的方式
a[0] = 1
print(b)                # [0, 2, 3]

