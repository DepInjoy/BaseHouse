'''
知识点3:集合运算
'''
set0 = {1,2,2,3,5}        #自动去重,[1,2,3]
set1 = {0,1,3,4}
'''
(set0 | set1)         求并集
(set0 & set1)         求交集
(set0 - set1)         求差集，即再set0中不在set1中
(set0 ^ set1)         求对称差集，在set0和set1中，不会同时出现在两者中
'''
# {0, 1, 2, 3, 4, 5} {1, 3} {2, 5} {0, 2, 4, 5}
print((set0 | set1), (set0 & set1), (set0 - set1), (set0 ^ set1))